let a = 10;
let b = 5;
let operation = "+";
switch (operation) {
    case "+":
        console.log(a + b);
        break;
    case "-":
        console.log(a - b)
        break;
    case "*":
        console.log(a * b)
        break;
    case "/":
        console.log(a / b)
        break;
    default:
        console.log("Select operation type")
}